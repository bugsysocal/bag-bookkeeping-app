pub fn export_contact_statement(header: &ExportHeader, cs: &ContactStatement) -> Result<Vec<u8>, String> {
    let columns = ["Date", "Description", "Debit", "Credit", "Running balance"];
    let mut data = vec![vec![t("Opening balance"), t(""), Cell::Kobo(0), Cell::Kobo(0), Cell::Kobo(cs.opening_balance_kobo)]];
    data.extend(cs.lines.iter().map(|l| {
        let desc = if l.voided { format!("{} (voided)", l.description) } else { l.description.clone() };
        vec![Cell::Date(l.date.clone()), t(desc), Cell::Kobo(l.debit_kobo), Cell::Kobo(l.credit_kobo), Cell::Kobo(l.running_balance_kobo)]
    }));
    data.push(vec![t("Closing balance"), t(""), Cell::Kobo(0), Cell::Kobo(0), Cell::Kobo(cs.closing_balance_kobo)]);
    build(header, &columns, &data)
}
