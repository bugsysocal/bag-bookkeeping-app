pub fn export_general_ledger(header: &ExportHeader, gl: &GeneralLedger) -> Result<Vec<u8>, String> {
    let columns = ["Date", "Memo", "Contact", "Amount", "Running balance"];
    let mut data = vec![vec![
        t("Opening balance"), t(""), t(""), Cell::Kobo(0), Cell::Kobo(gl.opening_balance_kobo),
    ]];
    data.extend(gl.lines.iter().map(|l| {
        vec![
            Cell::Date(l.date.clone()), t(&l.memo), t(l.contact_name.clone().unwrap_or_default()),
            Cell::Kobo(l.amount_kobo), Cell::Kobo(l.running_balance_kobo),
        ]
    }));
    data.push(vec![t("Closing balance"), t(""), t(""), Cell::Kobo(0), Cell::Kobo(gl.closing_balance_kobo)]);
    build(header, &columns, &data)
}

pub fn export_vat_report(header: &ExportHeader, vr: &VatReport) -> Result<Vec<u8>, String> {
    let columns = ["Type", "Document", "Party", "TIN", "Net", "VAT"];
    let mut data: Vec<Vec<Cell>> = vr
        .output
        .iter()
        .map(|l| vec![t("Output"), t(&l.doc_number), t(&l.party_name), t(l.tin.clone().unwrap_or_default()), Cell::Kobo(l.net_kobo), Cell::Kobo(l.vat_kobo)])
        .collect();
    data.extend(vr.input.iter().map(|l| {
        vec![t("Input"), t(&l.doc_number), t(&l.party_name), t(l.tin.clone().unwrap_or_default()), Cell::Kobo(l.net_kobo), Cell::Kobo(l.vat_kobo)]
    }));
    data.push(vec![t("Total output VAT"), t(""), t(""), t(""), t(""), Cell::Kobo(vr.output_vat_kobo)]);
    data.push(vec![t("Total input VAT"), t(""), t(""), t(""), t(""), Cell::Kobo(vr.input_vat_kobo)]);
    data.push(vec![t("Net payable (this month)"), t(""), t(""), t(""), t(""), Cell::Kobo(vr.net_payable_kobo)]);
    data.push(vec![t("Credit brought forward"), t(""), t(""), t(""), t(""), Cell::Kobo(vr.credit_brought_forward_kobo)]);
    data.push(vec![t("Net due"), t(""), t(""), t(""), t(""), Cell::Kobo(vr.net_due_kobo)]);
    build(header, &columns, &data)
}

